a, b, c = input().split()
A = float(a)
B = float(b)
C = float(c)
aTrianRetangulo = (A * C) / 2
aCircRaio = (C ** 2) * 3.14159
aTrapzeio = ((A + B) * C) / 2
aQuadrado = B ** 2
aRetangulo = A * B
print(f'TRIANGULO: {aTrianRetangulo:.3f}')
print(f'CIRCULO: {aCircRaio:.3f}')
print(f'TRAPEZIO: {aTrapzeio:.3f}')
print(f'QUADRADO: {aQuadrado:.3f}')
print(f'RETANGULO: {aRetangulo:.3f}')





